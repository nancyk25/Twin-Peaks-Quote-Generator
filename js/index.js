var currentQuote
var newQuote

function randomizeQuote(){
  do{
  var index = Math.floor((Math.random()*quotes.length))
 } while (currentQuote === index);
  newQuote = quotes[index];
  currentQuote = index;
  $("#quote-text").html('"' + quotes[index].quote + '"');
  $("#quote-author").html('-' + quotes[index].author);
};

function tweet () {
    var tweetQuote = quotes[currentQuote].quote;
    
    if (tweetQuote.length > 100) {
      tweetQuote = tweetQuote.substr(0,100).match(/(^.+)\s/)[1] + "...";
    
    var tweetAuthor = quotes[currentQuote].author;
      tweetQuote = encodeURI(tweetQuote);
      tweetAuthor = encodeURI(tweetAuthor);
      window.open("https://twitter.com/intent/tweet?text=" + tweetQuote  + " - " + tweetAuthor);
    };
}


$(document).ready(function(){

  $("#new-quote").click(function(){
   randomizeQuote();
  });
  
  $("#tweet-quote").click(function(){
    tweet();
  });
  
  $("#tumblr-quote").click(function(){
    $('#tumblr-quote').attr('href', 'https://www.tumblr.com/widgets/share/tool?posttype=quote&tags=quotes,freecodecamp&caption='+encodeURI(quotes[currentQuote].quote)+'&content=' + encodeURI(quotes[currentQuote].author)+'&canonicalUrl=https%3A%2F%2Fwww.tumblr.com%2Fbuttons&shareSource=tumblr_share_button');

  });
  
  
});